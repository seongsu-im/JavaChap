package chap12.Collection;

public class Book {

	
	public int id;
	public String title;
	public String author;
	public int quantity;
	
	public Book(int id, String title, String author, int quantity) {
		super();
		this.id = id;
		this.title = title;
		this.author = author;
		this.quantity = quantity;
	}
	
	
	 
}
